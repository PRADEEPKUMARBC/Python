# if
number = 10

if (number > 0):
    print("Number is positive")

# if else
if (number > 0):
    print("Number is positive")
else:
    print("Number is negative")

# if else if
if (number > 0):
    print("Number is positive")
elif (number < 0):
    print("Number is negative")
else:
    print("Number is Zero")